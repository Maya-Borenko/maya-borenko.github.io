function shuffleArray(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function renderForm() {
  let html = "";
  questions.forEach((q, index) => {
    const shuffled = shuffleArray([...q.options]).slice(0, 8);
    html += `<div class="question-card">
        <div class="question-text">${q.question}</div>
        <div class="options">`;
    shuffled.forEach((opt) => {
      html += `
          <div class="option">
            <input type="radio" name="q${index}" id="q${index}_${opt.letter}" value="${opt.letter}" required>
            <label for="q${index}_${opt.letter}">${opt.text}</label>
          </div>`;
    });
    html += `</div></div>`;
  });
  form.innerHTML = html;
}

function calculateResult() {
  const counts = {};
  for (let i = 0; i < 11; i++) {
    counts[String.fromCharCode(65 + i)] = 0;
  }
  let allAnswered = true;
  questions.forEach((q, idx) => {
    const selected = document.querySelector(`input[name="q${idx}"]:checked`);
    if (!selected) {
      allAnswered = false;
      return;
    }
    counts[selected.value]++;
  });

  if (!allAnswered) {
    alert(
      "Пожалуйста, ответьте на все вопросы, чтобы фея любви могла определить вашу пару!",
    );
    return null;
  }

  let maxCount = 0;
  let resultLetters = [];
  for (let letter in counts) {
    if (counts[letter] > maxCount) {
      maxCount = counts[letter];
      resultLetters = [letter];
    } else if (counts[letter] === maxCount) {
      resultLetters.push(letter);
    }
  }

  return resultLetters;
}

function showResult(letters) {
  if (!letters || letters.length === 0) return;
  const mainLetter = letters[0];
  const couple = couples.find((c) => c.letter === mainLetter);
  if (!couple) return;

  resultBlock.innerHTML = `
      <img class="result-img" src='${couple.img_href}'></img>
      <div class="result-title">${couple.name}</div>
      <div class="result-description">${couple.desc}</div>
      ${letters.length > 1 ? `<p style="color:#b0306a; margin-top:0.5rem;"> Кстати, у вас есть черты и других пар - вы уникальны!</p>` : ""}
      <button class="retake-btn" onclick="resetQuiz()">Пройти заново</button>
    `;
  resultBlock.style.display = "block";
  resultBlock.scrollIntoView({ behavior: "smooth", block: "center" });
}

function resetQuiz() {
  resultBlock.style.display = "none";
  resultBlock.innerHTML = "";
  const radios = document.querySelectorAll('input[type="radio"]');
  radios.forEach((r) => (r.checked = false));
  window.scrollTo({ top: 0, behavior: "smooth" });
  renderForm();
}

submitBtn.addEventListener("click", () => {
  const resultLetters = calculateResult();
  if (resultLetters) {
    showResult(resultLetters);
  }
});

